    public void loadGuests(){
        DefaultTableModel model= new DefaultTableModel(new Object[]{"guest id","name","passport"}, 0);
       
        try {
            ResultSet rs = DbConnection.loadFrom("guest");
            while(rs.next()){
                int guest_id= rs.getInt("guest_id");
                String name= rs.getString("name");
                int passport= rs.getInt("passport_id");
                model.addRow(new Object[]{guest_id,name,passport});
            }
            jTable1.setModel(model);
        }catch(SQLException ex){
            
        }
    }

   
