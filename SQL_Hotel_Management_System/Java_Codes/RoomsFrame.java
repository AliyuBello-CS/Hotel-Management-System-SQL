    public void loadRooms(){
        DefaultTableModel model= new DefaultTableModel(new Object[]{"room number","room type","floor number","price","occupants"}, 0);
       
        try {
            ResultSet rs = DbConnection.loadFrom("room");
            while(rs.next()){
                int roomnumber= rs.getInt("roomnumber");
                String type= rs.getString("roomtype");
                int floor= rs.getInt("floornumber");
                int price= rs.getInt("typicprice");
                String occupants= rs.getString("occupant");
                model.addRow(new Object[]{roomnumber,type,floor,price,occupants});
            }
            jTable1.setModel(model);
        }catch(SQLException ex){
            
        }
    }

