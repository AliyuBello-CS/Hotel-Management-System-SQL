    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtroomNumber.getText().isEmpty()
                ||txtType.getText().isEmpty()
                    ||txtfloor.getText().isEmpty()
                    ||txtprice.getText().isEmpty()
                || txtOccupants.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "please enter all info");
            return;
            
        }
        try {
            DbConnection.inserRoom(Integer.parseInt( txtroomNumber.getText()), txtType.getText(), Integer.parseInt( txtfloor.getText()),Integer.parseInt( txtprice.getText()),txtOccupants.getText());
        
            loadRooms();
        } catch (SQLException ex) {
            Logger.getLogger(RoomsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

